public class Corrida {

    private Integer numVoltas;
    private Integer numCarros;

    public Corrida() {
        this.numVoltas = numVoltas;
        this.numCarros = numCarros;
    }

    public void setVoltas(Integer numVoltas) {
        this.numVoltas = numVoltas;
    }

    public Integer getVoltas() {
        return numVoltas;
    }

    public void setNumCarros(Integer numCarros) {
        this.numCarros = numCarros;
    }

    public Integer getNumCarros() {
        return numCarros;
    }

}
